// Open the popup box
function openPopup() {
    document.getElementById("popup").style.display = "block";
  }
  
  // Close the popup box
  function closePopup() {
    document.getElementById("popup").style.display = "none";
  }
  
  // Set the carousel image
  function setCarouselImage(image) {
    document.getElementById("carousel-image").src = image;
  }